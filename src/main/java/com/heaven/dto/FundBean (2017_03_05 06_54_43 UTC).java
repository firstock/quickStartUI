package com.heaven.dto;

import java.util.Date;

public class FundBean {
	private int	FUND_NUM;			
	private String USER_EMAIL;			
	private String PROJECT_TITLE;		
	private int	FUND_DONATION;		
	private Date FUND_DATE;
	
	public int getFUND_NUM() {
		return FUND_NUM;
	}
	public void setFUND_NUM(int fUND_NUM) {
		FUND_NUM = fUND_NUM;
	}
	public String getUSER_EMAIL() {
		return USER_EMAIL;
	}
	public void setUSER_EMAIL(String uSER_EMAIL) {
		USER_EMAIL = uSER_EMAIL;
	}
	public String getPROJECT_TITLE() {
		return PROJECT_TITLE;
	}
	public void setPROJECT_TITLE(String pROJECT_TITLE) {
		PROJECT_TITLE = pROJECT_TITLE;
	}
	public int getFUND_DONATION() {
		return FUND_DONATION;
	}
	public void setFUND_DONATION(int fUND_DONATION) {
		FUND_DONATION = fUND_DONATION;
	}
	public Date getFUND_DATE() {
		return FUND_DATE;
	}
	public void setFUND_DATE(Date fUND_DATE) {
		FUND_DATE = fUND_DATE;
	}
	@Override
	public String toString() {
		return "FundBean [FUND_NUM=" + FUND_NUM + ", USER_EMAIL=" + USER_EMAIL + ", PROJECT_TITLE=" + PROJECT_TITLE
				+ ", FUND_DONATION=" + FUND_DONATION + ", FUND_DATE=" + FUND_DATE + "]";
	}			
}
